import { useState, useMemo, useEffect } from "react";
import Head from "next/head";

import { createEditor } from "slate";
import { Slate, withReact, ReactEditor } from "slate-react";
import { withHistory } from "slate-history";

import { withPalindrome, EditablePalindrome } from "@/lib/palindrome-plugin";

export default function Home() {
  const editor = useMemo(
    () => withPalindrome(withReact(withHistory(createEditor()))),
    []
  );
  const [value, setValue] = useState([
    {
      type: "paragraph",
      // children: [{ text: "" }],
      children: [{ text: "Eva, can I stab bats in a cave?" }],
      // children: [{ text: "A man, a plan, a canal: Panama!" }],
    },
  ]);

  // Run once on first render to initialize stuff
  useEffect(() => {
    editor.onChange();
    // set value to new array to force rerender
    setValue([...value]);

    setInterval(() => {
      ReactEditor.focus(editor);
    }, 10);
  }, []);

  return (
    <div>
      <Head>
        <title>Palindromaker</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <main className="m-4">
        <h1 className="text-3xl font-semibold text-gray-900 mb-3">
          Palindromaker
        </h1>

        <Slate
          editor={editor}
          value={value}
          onChange={(newValue) => setValue(newValue)}
        >
          <EditablePalindrome
            className={[
              "border-4 rounded shadow",
              "bg-white",
              "p-2",
              "font-mono text-lg tracking-wide text-gray-900",
              "max-w-prose",
              editor.palindrome.isPalindrome
                ? "border-green-600"
                : "border-red-400",
              "my-2 min-h-[300px]",
            ].join(" ")}
            editor={editor}
          />
        </Slate>

        {/* <button
          onClick={() => {
            console.log(editor.palindrome);
          }}
        >
          log
        </button> */}
      </main>
    </div>
  );
}
