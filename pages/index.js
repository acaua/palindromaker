import { useState, useMemo } from "react";
import Head from "next/head";

import { createEditor } from "slate";
import { Slate, withReact } from "slate-react";
import { withHistory } from "slate-history";

import { withPalindrome, EditablePalindrome } from "@/lib/palindrome-plugin";

export default function Home() {
  const editor = useMemo(
    () => withPalindrome(withReact(withHistory(createEditor()))),
    []
  );
  const [value, setValue] = useState([
    {
      type: "paragraph",
      children: [{ text: "Eva, can I stab bats in a cave?" }],
      // children: [{ text: "A man, a plan, a canal: Panama!" }],
    },
  ]);

  return (
    <div>
      <Head>
        <title>Palindromaker</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <main className="m-3">
        <h1 className="text-2xl">Palindromaker</h1>

        <Slate
          editor={editor}
          value={value}
          onChange={(newValue) => setValue(newValue)}
        >
          <EditablePalindrome
            className={[
              "border-4 rounded",
              "p-2",
              "tracking-wide",
              "prose prose-lg",
              editor.palindrome.isPalindrome
                ? "border-green-600"
                : "border-red-400",
              "my-2 min-h-[300px]",
            ].join(" ")}
            editor={editor}
          />
        </Slate>

        {/* <button
          onClick={() => {
            console.log(editor.palindrome);
          }}
        >
          log
        </button> */}
      </main>
    </div>
  );
}
